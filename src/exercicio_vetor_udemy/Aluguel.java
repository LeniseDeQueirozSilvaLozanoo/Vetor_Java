package exercicio_vetor_udemy;

public class Aluguel {
	
	String nome = "";
	String emal = "";
	public Aluguel(String nome, String emal) {
		this.nome = nome;
		this.emal = emal;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getEmal() {
		return emal;
	}
	public void setEmal(String emal) {
		this.emal = emal;
	}
	
	

	
	
}
