package exercicio_vetor;


public class Imprime_vetor {

	
	public void imprimeDouble(double vetor[]) {
		
		for(int i=0; i<vetor.length; i++) {
			System.out.print(vetor[i] + " ");
		}
		
	}

		
		
	public void imprimeInt(int vetor[]) {
		
		for(int i=0; i<vetor.length; i++) {
			System.out.print(vetor[i] + " ");
		}
		
	}	
		
		
	public void imprimeString(String vetor[]) {
		
		for(int i=0; i<vetor.length; i++) {
			System.out.print(vetor[i] + " ");
		}
				
	}
	
	
	public void imprimeChar(char vetor[]) {
		
		for(int i=0; i<vetor.length; i++) {
			System.out.print(vetor[i] + " ");
		}
				
	}
	
	
	
}
